-- Atomic NXC Deduction Function
CREATE OR REPLACE FUNCTION deduct_nxc(target_tg_id BIGINT, amount INT)
RETURNS JSON AS $$
DECLARE
  current_balance INT;
BEGIN
  SELECT nxc_balance INTO current_balance FROM users WHERE tg_id = target_tg_id FOR UPDATE;
  
  IF current_balance IS NULL THEN
    RETURN json_build_object('success', false, 'error', 'User not found');
  END IF;
  
  IF current_balance < amount THEN
    RETURN json_build_object('success', false, 'error', 'Insufficient balance');
  END IF;
  
  UPDATE users SET nxc_balance = nxc_balance - amount WHERE tg_id = target_tg_id;
  
  RETURN json_build_object('success', true, 'new_balance', current_balance - amount);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Atomic Balance Increment (for payments/admins)
CREATE OR REPLACE FUNCTION add_nxc_and_days(target_tg_id BIGINT, days_to_add INT, nxc_to_add INT)
RETURNS JSON AS $$
BEGIN
  INSERT INTO users (tg_id, balance_days, nxc_balance)
  VALUES (target_tg_id, days_to_add, nxc_to_add)
  ON CONFLICT (tg_id) 
  DO UPDATE SET 
    balance_days = users.balance_days + EXCLUDED.balance_days,
    nxc_balance = users.nxc_balance + EXCLUDED.nxc_balance;
    
  RETURN json_build_object('success', true);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Get User Rank based on ambassador points
CREATE OR REPLACE FUNCTION get_user_rank(user_tg_id BIGINT)
RETURNS JSON AS $$
DECLARE
  u_points INT;
  u_rank INT;
  t_users INT;
BEGIN
  -- Get user points first to avoid subquery issues
  SELECT COALESCE(ambassador_points, 0) INTO u_points 
  FROM users 
  WHERE tg_id = user_tg_id;
  
  -- If user doesn't exist yet, treat it as 0 points
  IF u_points IS NULL THEN
    u_points := 0;
  END IF;

  SELECT count(*) + 1 INTO u_rank 
  FROM users 
  WHERE ambassador_points > u_points;
  
  SELECT count(*) INTO t_users FROM users;
  
  -- Handle empty DB case gracefully for UI
  IF t_users = 0 THEN 
    t_users := 1;
    u_rank := 1;
  END IF;

  RETURN json_build_object('rank', u_rank, 'total', t_users);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Update referral_count automatically on insert
CREATE OR REPLACE FUNCTION increment_referral_count()
RETURNS trigger AS $$
BEGIN
  IF NEW.referred_by IS NOT NULL THEN
    -- Referrer reward: stats + points + days
    UPDATE users SET 
      referral_count = referral_count + 1,
      ambassador_points = ambassador_points + 100,
      balance_days = balance_days + 10
    WHERE tg_id = NEW.referred_by;
    
    -- Referred user reward: 7 days premium + 150 NXC
    UPDATE users SET 
      balance_days = balance_days + 7,
      nxc_balance = nxc_balance + 150
    WHERE tg_id = NEW.tg_id;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS tr_after_user_insert ON users;
CREATE TRIGGER tr_after_user_insert
  AFTER INSERT ON users
  FOR EACH ROW
  EXECUTE FUNCTION increment_referral_count();

