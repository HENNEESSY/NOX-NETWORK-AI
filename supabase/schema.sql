CREATE OR REPLACE FUNCTION delete_old_history()
RETURNS trigger AS $$
BEGIN
  DELETE FROM ai_history WHERE created_at < NOW() - INTERVAL '7 days';
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Function for Smart Auto-Refill
CREATE OR REPLACE FUNCTION process_auto_refills()
RETURNS void AS $$
BEGIN
  -- Renew for users with expired sub, auto_refill ON and enough NXC
  UPDATE users 
  SET 
    nxc_balance = nxc_balance - 399,
    balance_days = 30
  WHERE 
    auto_refill = TRUE 
    AND balance_days <= 0 
    AND nxc_balance >= 399;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_delete_old_history ON ai_history;

CREATE TRIGGER trigger_delete_old_history
  AFTER INSERT ON ai_history
  EXECUTE FUNCTION delete_old_history();
